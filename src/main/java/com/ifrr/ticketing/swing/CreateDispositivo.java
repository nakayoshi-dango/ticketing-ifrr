    private void botonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearActionPerformed
        if (!textTipo.getText().isBlank() || !textMarca.getText().isBlank() || !textModelo.getText().isBlank() || !textDescripcion.getText().isBlank()) {
            Dispositivo di = new Dispositivo(textTipo.getText(), textMarca.getText(), textModelo.getText(), textDescripcion.getText());
            di = sd.createDispositivo(di);
            if (di != null) {
                dispose();
            } else {
                JOptionPane.showMessageDialog(
                    null,
                    "No se pudo crear el dispositivo.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            }
        } else {
            JOptionPane.showMessageDialog(
                    null,
                    "Debe haber al menos un campo relleno.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_botonCrearActionPerformed

